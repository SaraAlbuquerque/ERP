    public void readJTable(usuario user) {

        DefaultTableModel modelo = (DefaultTableModel) tblTransacoes.getModel();
        modelo.setNumRows(0);
        transacaoDAO dao = new transacaoDAO();
        String tipo;

        for (transacao t : dao.readForUser(user)) {

            LocalDate date = LocalDate.of(t.getAsOnDate().getYear(), t.getAsOnDate().getDate(), t.getAsOnDate().getDay());

            if (t.getIsIncome()) {
                tipo = "Entrada";
            } else {
                tipo = "Saída";
            }

            modelo.addRow(new Object[]{
                tipo,
                t.getTransactionId(),
                t.getNetAmount(),
                t.getDescription(),
                date
            });
        }

    }
