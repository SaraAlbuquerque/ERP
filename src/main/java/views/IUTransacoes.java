    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        transacao t = new transacao();
        transacaoDAO dao = new transacaoDAO();
        Timestamp today = new Timestamp(System.currentTimeMillis());

        t.setNetAmount(BigDecimal.valueOf(Double.valueOf(txtValor.getText())));
        t.setDescription(txtDesc.getText());
        t.setAsOnDate(today);
        if (btnEntrada.isSelected()) {
            t.setIsIncome(true);
        } else {
            t.setIsIncome(false);
        }

        dao.create(t);

        txtValor.setText("");
        txtDesc.setText("");
        btnEntrada.setSelected(true);

        readJTable(user);

    }//GEN-LAST:event_btnAddActionPerformed

    private void tblTransacoesKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tblTransacoesKeyReleased

        if (tblTransacoes.getSelectedRow() != -1) {
            txtValor.setText(tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 2).toString());
            txtDesc.setText(tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 3).toString());

            if (tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 0).toString().equals("Entrada")) {
                btnEntrada.setSelected(true);
            } else {
                btnSaida.setSelected(true);
            }
        }

    }//GEN-LAST:event_tblTransacoesKeyReleased

    private void btnAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtualizarActionPerformed

        if (tblTransacoes.getSelectedRow() != -1) {
            transacao t = new transacao();
            transacaoDAO dao = new transacaoDAO();

            t.setTransactionId(Integer.parseInt(tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 1).toString()));
            t.setNetAmount(BigDecimal.valueOf(Double.valueOf(txtValor.getText())));
            t.setDescription(txtDesc.getText());
            t.setAsOnDate(Timestamp.valueOf(tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 4).toString()));
            if (btnEntrada.isSelected()) {
                t.setIsIncome(true);
            } else {
                t.setIsIncome(false);
            }

            dao.update(t);

            txtValor.setText("");
            txtDesc.setText("");
            btnEntrada.setSelected(true);

            readJTable(this.user);
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um item para atualizar!");
        }

    }//GEN-LAST:event_btnAtualizarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed

        if (tblTransacoes.getSelectedRow() != -1) {
            transacao t = new transacao();
            transacaoDAO dao = new transacaoDAO();

            t.setTransactionId(Integer.parseInt(tblTransacoes.getValueAt(tblTransacoes.getSelectedRow(), 1).toString()));
            

            dao.delete(t);

            txtValor.setText("");
            txtDesc.setText("");
            btnEntrada.setSelected(true);

            readJTable(this.user);
        }
        else{
            JOptionPane.showMessageDialog(null, "Selecione um item para excluir!");
        }

    }//GEN-LAST:event_btnExcluirActionPerformed

